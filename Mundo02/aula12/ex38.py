PrimeiroNumero = int(input("Informe o primeiro número: "))
SegundoNumero = int(input("Informe o segundo número: "))

if PrimeiroNumero > SegundoNumero: print(f"O número {PrimeiroNumero} é MAIOR que o número {SegundoNumero}")
elif PrimeiroNumero < SegundoNumero: print(f"O número {PrimeiroNumero} é MENOR que o número {SegundoNumero}")
elif PrimeiroNumero == SegundoNumero: print(f"O número {PrimeiroNumero} é IGUAL ao número {SegundoNumero}")